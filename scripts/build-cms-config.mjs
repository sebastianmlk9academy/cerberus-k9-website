function countFields(x) {
  if (Array.isArray(x)) return x.reduce((a, b) => a + countFields(b), 0);
  if (x && typeof x === 'object') {
    if (x.widget) return 1 + (x.fields ? countFields(x.fields) : 0);
    if (x.fields) return countFields(x.fields);
  }
  return 0;
}

